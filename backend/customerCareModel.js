import mongoose from 'mongoose';

const customerCareSchema = new mongoose.Schema({
    username: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true,
        unique: true
    },
    carName: {
        type: String,
        required: true
    },
    location: {
        type: String,
        required: true
    },
    description: {
        type: String,
        required: true
    }
});

const CustomerCare = mongoose.model('CustomerCare', customerCareSchema);

export default CustomerCare;
